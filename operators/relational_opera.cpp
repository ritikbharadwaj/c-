#include<iostream>
using namespace std;
int main()
{
    // relational operators are:- = < > <= >= !=
    int a =2;
    int b=3;
    // in bool 0 means false and 1 means true
    bool first= a==b;
    cout<<first<<endl;

    bool second= a<=b;
    cout<<second<<endl;

    bool third= a!=b;
    cout<<third<<endl;

    return 0;
}